            Pravila privatnosti
Dobro došli u stomatolozi.online Ova stranica pravila privatnosti obavještava vas o našim pravilima u vezi s prikupljanjem, korištenjem i otkrivanjem osobnih podataka kada koristite našu aplikaciju.

Prikupljanje i korištenje informacija
Kada koristite našu aplikaciju, možemo vas zamoliti da nam pružite određene osobne podatke koje možemo koristiti za kontaktiranje ili identifikaciju. Osobni podaci mogu uključivati, ali nisu ograničeni na: ime, adresu e-pošte, broj telefona i adresu.

Dnevničke podatke
Kao i mnoge web stranice, prikupljamo podatke koje vaš preglednik šalje kad posjetite našu aplikaciju ("Dnevnički podaci"). Ovi dnevnički podaci mogu uključivati informacije kao što su IP adresa vašeg računala, vrsta preglednika, verzija preglednika, stranice naše aplikacije koje posjećujete, vrijeme i datum vašeg posjeta, vrijeme provedeno na tim stranicama i druge statistike.

Kolačići
Kolačići su datoteke s malom količinom podataka koje mogu uključivati anonimni jedinstveni identifikator. Kolačići se šalju vašem pregledniku s web stranice i pohranjuju se na tvrdi disk vašeg računala.

Mi koristimo "kolačiće" za prikupljanje informacija. Možete postaviti svoj preglednik da odbije sve kolačiće ili da vas obavijesti kada se kolačić šalje. Ako ne prihvatite kolačiće, možda nećete moći koristiti neke dijelove naše aplikacije.

Sigurnost
Sigurnost vaših osobnih podataka nam je važna, ali zapamtite da nijedan način prijenosa putem Interneta ili elektroničkog načina pohrane nije 100% siguran. Iako nastojimo koristiti komercijalno prihvatljive načine za zaštitu vaših osobnih podataka, ne možemo jamčiti njegovu apsolutnu sigurnost.

Promjene u ovim pravima privatnosti
Zadržavamo pravo ažuriranja ili promjene naših pravila privatnosti u bilo kojem trenutku. Preporučujemo vam da povremeno pregledavate ovu stranicu pravila privatnosti kako biste bili obaviješteni o bilo kakvim promjenama. Ako nastavite koristiti našu aplikaciju nakon što smo objavili promjene na ovoj stranici, smatrat će se da ste prihvatili te promjene i suglasni ste s njima.

Dijeljenje vaših osobnih podataka
Nećemo prodati, distribuirati ili iznajmljivati vaše osobne podatke trećim stranama osim ako imamo vašu dozvolu ili smo zakonski obvezni učiniti. Možemo koristiti vaše osobne podatke kako bismo vam poslali promotivne informacije o trećim stranama za koje mislimo da bi vam mogle biti zanimljive ako nam kažete da to želite.



Poveznice na druge web stranice
Naša aplikacija može sadržavati poveznice na druge web stranice koje nisu pod našom kontrolom. Ako koristite te poveznice i napustite našu aplikaciju, imajte na umu da mi nemamo kontrolu nad tim drugim web stranicama. Stoga ne možemo biti odgovorni za zaštitu i privatnost bilo kojih informacija koje pružate tijekom posjeta takvim stranicama, a takve stranice nisu obuhvaćene ovim pravilima privatnosti. Trebali biste biti oprezni i pogledati izjavu o privatnosti koja se odnosi na web stranicu u pitanju.

Vaša prava
Imate pravo pristupiti, ispraviti, brisati ili ograničiti obradu svojih osobnih podataka. Također imate pravo uložiti prigovor na obradu vaših osobnih podataka. Ako želite ostvariti ova prava ili imate pitanja o našim pravilima privatnosti, molimo kontaktirajte nas na [podrska@stomatolozi.online].

